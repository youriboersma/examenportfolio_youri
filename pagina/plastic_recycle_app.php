<h4>Samen met een groep studenten hebben we een webapplicatie ontwikkeld die het mogelijk maakt om plastic producten te scannen en te identificeren welke soort plastic het is.
  Het project is gericht op kinderen vanaf 6 jaar, met een gebruiksvriendelijke interface en visuele hulpmiddelen die het leerproces ondersteunen.
  De applicatie herkent minimaal 50 verschillende plastic producten, en gebruikers kunnen zelf nieuwe producten toevoegen door foto's te uploaden.
  Na het scannen wordt de samenstelling van het product weergegeven, inclusief het onderscheid tussen verschillende delen zoals de dop en het hoofdonderdeel.
  Het ontwerp is kleurrijk en intu√Øtief, om zelfs jonge gebruikers met weinig leesvaardigheid te betrekken</h4>

