<h4>
  Samen met een groep op school hebben we een project uitgevoerd voor het pizza restaurant Sopranos.
  Voor dit project hebben we een website ontwikkeld waarmee klanten eenvoudig online eten kunnen bestellen.
  De website bevat een gebruiksvriendelijke interface en biedt de mogelijkheid om een persoonlijk account aan te maken.
  Met deze login kunnen klanten hun bestelgeschiedenis bekijken, favoriete items opslaan en sneller nieuwe bestellingen plaatsen.
  Het platform is ontworpen om de klantenervaring te verbeteren en het bestelproces zo soepel mogelijk te maken.</h4>

